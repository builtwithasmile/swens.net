    private static function upsertLocation(string $code, string $name, int $active, ?string $opensOn, ?string $ciStore): void
    {
        Database::query(
            "INSERT INTO locations (code, name, active, opens_on, ci_store_name)
             VALUES (?, ?, ?, ?, ?)
             ON DUPLICATE KEY UPDATE name = VALUES(name), active = VALUES(active),
               opens_on = VALUES(opens_on), ci_store_name = VALUES(ci_store_name)",
            [$code, $name, $active, $opensOn, $ciStore]
        );
    }

    public static function upsertUser(string $email, string $name, string $role, string $password): void
    {
        $email = strtolower(trim($email));
        $existing = Database::fetch("SELECT id FROM users WHERE email = ?", [$email]);
        if ($existing) {
            Database::query(
                "UPDATE users SET name = ?, role = ?, password_hash = ?, active = 1 WHERE id = ?",
                [$name, $role, Auth::hash($password), (int) $existing['id']]
            );
        } else {
            Database::query(
                "INSERT INTO users (email, name, role, password_hash) VALUES (?, ?, ?, ?)",
                [$email, $name, $role, Auth::hash($password)]
            );
        }
    }

    /**
     * Seed/refresh scrape targets + their ci_stores rows.
     * Each $targets entry: name, platform, api_url, x_group, x_store, x_app_mode, is_home, url.
     * Idempotent (keyed on unique store name).
     */
    public static function seedTargets(array $targets): void
    {
        foreach ($targets as $t) {
            $home = !empty($t['is_home']) ? 1 : 0;
            Database::query(
                "INSERT INTO dd_scrape_targets (store_name, url, platform, x_group, x_store, x_app_mode, is_home, active)
                 VALUES (:name, :url, :platform, :xg, :xs, :mode, :home, 1)
                 ON DUPLICATE KEY UPDATE url=VALUES(url), platform=VALUES(platform),
                   x_group=VALUES(x_group), x_store=VALUES(x_store), x_app_mode=VALUES(x_app_mode), is_home=VALUES(is_home)",
                [
                    'name' => $t['name'], 'url' => $t['url'] ?? '', 'platform' => $t['platform'] ?? 'blaze',
                    'xg' => $t['x_group'] ?? '', 'xs' => $t['x_store'] ?? '', 'mode' => $t['x_app_mode'] ?? 'default', 'home' => $home,
                ]
            );
            Database::query(
                "INSERT INTO ci_stores (name, platform, api_url, x_group, x_store, x_app_mode, is_home)
                 VALUES (:name, :platform, :url, :xg, :xs, :mode, :home)
                 ON DUPLICATE KEY UPDATE platform=VALUES(platform), api_url=VALUES(api_url),
                   x_group=VALUES(x_group), x_store=VALUES(x_store), x_app_mode=VALUES(x_app_mode), is_home=VALUES(is_home)",
                [
                    'name' => $t['name'], 'platform' => $t['platform'] ?? 'blaze',
                    'url' => $t['api_url'] ?? 'https://ecom-api.blaze.me/api/v1/products',
                    'xg' => $t['x_group'] ?? '', 'xs' => $t['x_store'] ?? '', 'mode' => $t['x_app_mode'] ?? 'default', 'home' => $home,
                ]
            );
        }
    }
}
