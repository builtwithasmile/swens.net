<?php
declare(strict_types=1);

namespace App\Core;

/**
 * Installer: writes config.php and seeds baseline data (locations, users,
 * settings, scrape targets). Used by both the web installer (install.php) and
 * the headless CLI installer (scripts/install_cli.php). All seeds idempotent.
 */
class Installer
{
    public static function isInstalled(): bool
    {
        return is_file(APP_ROOT . '/config.php');
    }

    /** Write config.php from $v. Generates SESSION_SECRET. Returns the path. */
    public static function writeConfig(array $v): string
    {
        $esc = static fn($s): string => str_replace(['\\', "'"], ['\\\\', "\\'"], (string) $s);
        $lines = ['<?php'];
        $lines[] = '// Generated by the installer on ' . gmdate('Y-m-d H:i:s') . ' UTC. Do NOT commit.';
        $lines[] = 'declare(strict_types=1);';
        $lines[] = '';

        $str = static fn(string $k, $val) => "define('$k', '" . $esc($val) . "');";
        $raw = static fn(string $k, $val) => "define('$k', $val);";

        $lines[] = $str('APP_ENV', $v['app_env'] ?? 'production');
        $lines[] = $str('APP_URL', $v['app_url'] ?? 'http://localhost:8000');
        $lines[] = $str('APP_TIMEZONE', $v['timezone'] ?? 'America/Vancouver');
        $lines[] = '';
        $lines[] = $str('DB_HOST', $v['db_host'] ?? 'localhost');
        $lines[] = $raw('DB_PORT', (int) ($v['db_port'] ?? 3306));
        $lines[] = $str('DB_NAME', $v['db_name'] ?? '');
        $lines[] = $str('DB_USER', $v['db_user'] ?? '');
        $lines[] = $str('DB_PASS', $v['db_pass'] ?? '');
        $lines[] = '';
        $lines[] = $str('SESSION_SECRET', bin2hex(random_bytes(32)));
        $lines[] = $str('SESSION_NAME', 'cdd_session');
        $lines[] = '';
        $lines[] = $str('ANTHROPIC_API_KEY', $v['anthropic_key'] ?? '');
        $lines[] = $str('ANTHROPIC_API_URL', 'https://api.anthropic.com/v1/messages');
        $lines[] = $str('ANTHROPIC_VERSION', '2023-06-01');
        $lines[] = $str('ANTHROPIC_MODEL_FAST', $v['model_fast'] ?? 'claude-haiku-4-5');
        $lines[] = $str('ANTHROPIC_MODEL_SMART', $v['model_smart'] ?? 'claude-sonnet-4-6');
        $lines[] = $raw('ANTHROPIC_MONTHLY_BUDGET_USD', 50);
        $lines[] = '';
        $lines[] = $str('SCRAPE_USER_AGENT', $v['scrape_ua'] ?? 'CrossroadsDD/1.0 (+mailto:josh@swens.net)');
        $lines[] = $raw('SCRAPE_MIN_DELAY_SECONDS', 5);
        $lines[] = $str('CROSSROADS_HOME_STORE', $v['home_store'] ?? 'crossroads');
        $lines[] = '';
        $lines[] = $raw('BACKUP_DIR', "__DIR__ . '/storage/backups'");
        $lines[] = $raw('BACKUP_RETENTION_DAYS', 30);
        $lines[] = $str('BACKUP_SECONDARY_DIR', $v['backup_secondary'] ?? '');
        $lines[] = $str('MYSQLDUMP_BIN', $v['mysqldump_bin'] ?? 'mysqldump');
        $lines[] = '';
        $lines[] = $str('MAIL_FROM', $v['mail_from'] ?? 'dd@crossroadscannabis.ca');
        $lines[] = $str('MAIL_OWNER', $v['owner_email'] ?? 'josh@swens.net');
        $content = implode("\n", $lines) . "\n";

        file_put_contents(APP_ROOT . '/config.php', $content, LOCK_EX);
        return APP_ROOT . '/config.php';
    }

    /** Seed locations, users, settings. Idempotent. */
    public static function seed(array $owner, array $purchaser = []): void
    {
        self::upsertLocation('burns-lake', 'Burns Lake', 1, null, 'crossroads');
        self::upsertLocation('penticton', 'Penticton', 0, '2026-06-30', null);

        self::upsertUser((string) $owner['email'], (string) ($owner['name'] ?? 'Owner'), 'owner', (string) $owner['password']);
        if (!empty($purchaser['email'])) {
            self::upsertUser((string) $purchaser['email'], (string) ($purchaser['name'] ?? 'Purchaser'), 'purchaser', (string) $purchaser['password']);
        }

        $defaults = [
            'app_name'                    => 'Crossroads DD Intelligence',
            'doh_target_days'             => '21',
            'dd_share_baseline'           => '0',
            'vape_illicit_benchmark'      => '',
            'movement_alert_threshold_pct' => '0.10',
            'benchmark_competitor'        => 'mojo',
        ];
        foreach ($defaults as $k => $val) {
            Database::query(
                "INSERT INTO settings (`key`, `value`) VALUES (?, ?)
                 ON DUPLICATE KEY UPDATE `key` = `key`",
                [$k, $val]
            );
        }

        // Scrape targets + their ci_stores rows (the proven blaze-scraper set).
        self::seedTargets(self::defaultTargets());
    }

    /**
     * Canonical scrape-target set: Crossroads home store + 4 competitors, with
