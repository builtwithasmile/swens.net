            ['name' => 'mojo',        'platform' => 'tymber', 'api_url' => $blaze, 'x_group' => 'd7aced12-b380-4eb4-b278-ee1aa228c316', 'x_store' => 'd7aced12-b380-4eb4-b278-ee1aa228c316'],
            ['name' => 'rural-leaf',  'platform' => 'tymber', 'api_url' => $blaze, 'x_group' => '31faf394-2fae-46f1-888c-ecb62c8bb068', 'x_store' => '31faf394-2fae-46f1-888c-ecb62c8bb068'],
            ['name' => 'mjb',         'platform' => 'tymber', 'api_url' => $blaze, 'x_group' => 'ab776fac-2779-4438-9bb1-8607ccdb4353', 'x_store' => 'ab776fac-2779-4438-9bb1-8607ccdb4353'],
            ['name' => 'grasshopper', 'platform' => 'tymber', 'api_url' => $blaze, 'x_group' => '881541bb-5b5c-49bc-b913-a7cf37a4d3f5', 'x_store' => '881541bb-5b5c-49bc-b913-a7cf37a4d3f5'],
